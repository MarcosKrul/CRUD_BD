    private void cadCar_btn_buscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cadCar_btn_buscarActionPerformed
        String nome = cadCar_nomeBusca.getText();
        if (nome.equals("")){
            JOptionPane.showMessageDialog(null, "Por favor, informe um nome para busca");
            return;
        }
        
        List<Person> list = null;
        Person p = null;
        
        try {
            list = new PersonDAO().returnAllPeople();
        } catch (SQLException ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(null, EXC_SQL);
        }
        
        for(Person aux: list) if(aux.getName().equals(nome)) p = aux;
        if (p != null){
            
            cadCar_id_foreign = p.getId_person();
            String out = "Usuário(a) encontrado(a)!\n\n";
            out += "Nome: "+p.getName()+"\n";
            out += (p.getEmail().equals("null") ? "" : "E-mail: "+p.getEmail()+"\n");
            out += (p.getPhoneNumber().equals("null") ? "" : "Telefone "+p.getPhoneNumber()+"\n");
            out += "Idade: "+p.getAge()+"\n\n";
            out += "O resultado da busca está correto?"; 
            int resposta = JOptionPane.showConfirmDialog(null, out, "Confirmação de busca", JOptionPane.YES_NO_OPTION);
            
            if (resposta == JOptionPane.YES_OPTION){
                Car c = null;
                try {
                    c = new CarDAO().carSearch(p.getId_person());
                } catch (SQLException ex) {
                    ex.printStackTrace();
                    JOptionPane.showMessageDialog(null, EXC_SQL);
                }
                
                if (c != null){
                    String output = "O usuário possui um ou mais carros cadastrados\n";
                    output += "Deseja alterar os dados de algum registro existente?\n\n";
                    output += "Caso deseje realizar um novo cadastro, nega esta confirmação";
                    int resp = JOptionPane.showConfirmDialog(null, output, "Confirmação de alteração", JOptionPane.YES_NO_OPTION);
                    cadCarSetEnable(true, new Color(141, 53, 226));
                    
                    if(resp == JOptionPane.NO_OPTION) { cadCar = true; }
                    else {
                        cadCar = false;
                        Object[] outp = null;
                        try {
                            outp = new CarDAO().returnAllCars(true, p.getId_person()).toArray();
                        } catch (SQLException e1) {
                            e1.printStackTrace();
                            JOptionPane.showMessageDialog(null, EXC_SQL);
                        }
                        Car alterar =  (Car) JOptionPane.showInputDialog(null, "Selecione o veículo desejado",
                                "Alteração", JOptionPane.QUESTION_MESSAGE, null, outp, outp[0]);
                        
                        cadCar_id_car = alterar.getId_car();
                        cadCar_in_cor.setText(alterar.getColor());
                        cadCar_in_modelo.setText(alterar.getModel());
                        cadCar_in_marca.setText(alterar.getBrand());
                        cadCar_in_placa.setText(alterar.getLicensePlate());
                    }
                } else {
                    cadCarSetEnable(true, new Color(141, 53, 226));
                    cadCar = true;
                }
            } else {
                JOptionPane.showMessageDialog(null, "Por favor, considere letras maiúsculas e minúsculas e tente novamente");
                clearCadCar();
            }
            
        } else {
            JOptionPane.showMessageDialog(null, "Usuário não encontrado");
            clearCadCar();
        }
    }//GEN-LAST:event_cadCar_btn_buscarActionPerformed

