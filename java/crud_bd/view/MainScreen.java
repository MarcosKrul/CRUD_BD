    private void btn_enviarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_enviarActionPerformed

        String name = in_nome.getText();
        if (name.equals("")) {
            JOptionPane.showMessageDialog(null, "Por favor, informe um nome");
            return;
        }

        String email = in_email.getText();
        if (email.equals("")) email = "null";
        else if (!email.matches(REGEX)) {
            JOptionPane.showMessageDialog(null, "Por favor, informe um e-mail válido");
            return;
        }

        String telefone = in_telefone.getText();
        if (telefone.equals("(  ) 9    -    ")) {
            telefone = "null";
        }

        Integer idade = null;
        try {
            idade = Integer.parseInt(in_idade.getText());
        } catch (NumberFormatException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, "Por favor, informe uma idade válida");
        }

        Person p = new Person(name.trim(), email, telefone, idade);
        PersonDAO pD = new PersonDAO();

        try {
            pD.addPerson(p);
            JOptionPane.showMessageDialog(null, "Morador cadastrado com sucesso!");
            clearInput();
        } catch (SQLException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, EXC_SQL);
        }

    }//GEN-LAST:event_btn_enviarActionPerformed

