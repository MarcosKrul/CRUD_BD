    private void cadCar_btn_enviarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cadCar_btn_enviarActionPerformed
        
        Car c;
        String modelo = cadCar_in_modelo.getText();
        String marca = cadCar_in_marca.getText();
        String cor = cadCar_in_cor.getText();
        String placa = cadCar_in_placa.getText().toUpperCase();

        if(modelo.equals("")){
            JOptionPane.showMessageDialog(null, "Por favor, informe o modelo do veículo");
            return;
        }

        if(placa.equals("")){
            JOptionPane.showMessageDialog(null, "Por favor, informe a placa do veículo");
            return;
        }
            
        c = new Car(modelo, marca, placa, cor);
        c.setId_foreign(cadCar_id_foreign);
        c.setId_car(cadCar_id_car);
        CarDAO cD = new CarDAO();
        
        if(cadCar){
            try {
                cD.addCar(c);
                JOptionPane.showMessageDialog(null, "Veículo cadastrado com sucesso!");
            } catch (SQLException e1) {
                e1.printStackTrace();
                JOptionPane.showMessageDialog(null, EXC_SQL);
            }
        } else {
            try {
                cD.updateCar(c);
                JOptionPane.showMessageDialog(null, "Dados atualizados com sucesso!");
            } catch (SQLException e1) {
                e1.printStackTrace();
                JOptionPane.showMessageDialog(null, EXC_SQL);
            }
        }
        
        clearCadCar();
        cadCarSetEnable(false, new Color(103,42,114));
        
    }//GEN-LAST:event_cadCar_btn_enviarActionPerformed

    private void jMenuItem3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem3ActionPerformed
        
        String placa = JOptionPane.showInputDialog(null, "Informe a placa do veículo");
        if(placa == null) return;
        
        Car c = null;
        Person p = null;
        CarDAO cD = new CarDAO();
        try {
            for(Car aux: cD.returnAllCars(false, -1))
                if(aux.getLicensePlate().equals(placa.toUpperCase())) c = aux;
            if(c != null) p = new PersonDAO().personSearch(c.getId_foreign());
            else {
                JOptionPane.showMessageDialog(null, "Veículo não encontrado");
                return;
            }
        } catch (SQLException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, EXC_SQL);
        }
        
        String out = "Veículo encontrado\n\n";
        out += "Modelo: '"+c.getModel()+"' - "+c.getBrand()+" ("+c.getColor()+")\n\n";
        out += "Informações do proprietário\n";
        out += "Nome: "+p.getName()+"\n";
        out += (p.getEmail().equals("null")? "" : p.getEmail())+"\n";
        out += (p.getPhoneNumber().equals("null")? "" : p.getPhoneNumber())+"\n\n";
        out += "Você tem certeza que deseja excluir este veículo?";
        
        int resposta = JOptionPane.showConfirmDialog(null, out, "Confirmação de exclusão", JOptionPane.YES_NO_OPTION);
        
        if(resposta == JOptionPane.YES_OPTION){
            try {
                cD.deleteCar(c.getId_car());
                JOptionPane.showMessageDialog(null, "Veículo excluído com sucesso!");
            } catch (SQLException e) {
                e.printStackTrace();
                JOptionPane.showMessageDialog(null, EXC_SQL);
            }
        }
        
    }//GEN-LAST:event_jMenuItem3ActionPerformed

    private void jMenuItem2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem2ActionPerformed
        
        String nome = JOptionPane.showInputDialog(null, "Informe o nome do morador");
        if(nome == null) return;
        
        Person p = null;
        List<Car> list = null;
        PersonDAO pD = new PersonDAO();
        try {
            for(Person aux : pD.returnAllPeople())
                if(aux.getName().equals(nome)) p = aux;
            if(p == null) {
                JOptionPane.showMessageDialog(null, "Morador não encontrado");
                return;
            }
            list = new ArrayList<>();
            list = InnerJoinDAO.allCars(p.getId_person(), pD.getConnection());
        } catch(SQLException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, EXC_SQL);
        }
        
        String out = "Morador encontrado\n\n";
        if(list != null){
            out += (list.size()==1?"O morador possui um carro cadastrado: ":"O morador possui os seguintes carros cadastrados:")+"\n";
            for(Car c: list){
                out += "Modelo: '"+c.getModel()+"' - "+c.getBrand()+" ("+c.getColor()+")\n";
                out += "Placa: "+c.getLicensePlate()+"\n\n";
            }
            out += "Ao excluir um morador, os carros cadastrados em seu nome também serão excluídos.\n";
        }
        out += "Você tem certeza que deseja realizar esta operação?\n";

        int resposta = JOptionPane.showConfirmDialog(null, out, "Confirmação de exclusão", JOptionPane.YES_NO_OPTION);
        if(resposta == JOptionPane.YES_OPTION){
            try{
                CascadeExclusionDAO.cascadeExclusion(p.getId_person(), pD.getConnection());
                JOptionPane.showMessageDialog(null, "Usuário excluído com sucesso!");
            } catch(SQLException e){
                e.printStackTrace();
                JOptionPane.showMessageDialog(null, EXC_SQL);
            }
        }
        
    }//GEN-LAST:event_jMenuItem2ActionPerformed

